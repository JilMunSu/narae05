SELECT 
    SALELIST.SALENO, SHOP.SCODE, SALELIST.SALEDATE, PRODUCT.PCODE,
    PRODUCT.NAME, SALELIST.AMOUNT, TO_CHAR(SUM(PRODUCT.COST * SALELIST.AMOUNT), '999,999,999,999')
FROM 
    TBL_SALELIST_01 SALELIST, TBL_PRODUCT_01 PRODUCT, TBL_SHOP_01 SHOP
WHERE 
    SALELIST.PCODE = PRODUCT.PCODE AND 
    SHOP.SCODE = SALELIST.SCODE
GROUP BY 
    SALELIST.SALENO, SHOP.SCODE, SHOP.SNAME, SALELIST.SALEDATE, PRODUCT.PCODE,
    PRODUCT.NAME, SALELIST.AMOUNT
ORDER BY 
    SALELIST.SALENO ASC;

SELECT 
    SHOP.SCODE, SHOP.SNAME, TO_CHAR(SUM(PRODUCT.COST * SALELIST.AMOUNT), '999,999,999,999')
FROM 
    TBL_SHOP_01 SHOP, TBL_SALELIST_01 SALELIST, TBL_PRODUCT_01 PRODUCT
WHERE 
    SHOP.SCODE = SALELIST.SCODE AND 
    PRODUCT.PCODE = SALELIST.PCODE
GROUP BY 
    SHOP.SCODE, SHOP.SNAME
ORDER BY 
    SHOP.SCODE ASC;

SELECT 
    PRODUCT.PCODE, PRODUCT.NAME, 
    TO_CHAR(SUM(PRODUCT.COST * SALELIST.AMOUNT), '999,999,999,999')
FROM 
    TBL_PRODUCT_01 PRODUCT, 
    TBL_SALELIST_01 SALELIST
WHERE 
    PRODUCT.PCODE = SALELIST.PCODE
GROUP BY 
    PRODUCT.PCODE, PRODUCT.NAME
ORDER BY 
    SUM(PRODUCT.COST * SALELIST.AMOUNT) DESC; 

